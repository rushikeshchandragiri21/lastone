#!/bin/bash
# Print "Hello World" with Bold, Blink, and Colors

# Bold Effect
echo -e "\033[1mHello World\033[0m"

# Blink Effect
echo -e "\033[5mHello World\033[0m"

# Red Color
echo -e "\033[91mHello World\033[0m"  # Bright Red

# Green Color
echo -e "\033[92mHello World\033[0m"  # Bright Green

# Blue Color
echo -e "\033[94mHello World\033[0m"  # Bright Blue
